#include<bits/stdc++.h>
using namespace std;

int main(){

    // Demonstration of pre-increment and post-incremnt operators

    int i=0, j=0, k=0;

    cout<<i++<<j++<<k++<<" ";
    cout<<endl;
    cout<<++i<<++j<<++k<<" ";
    cout<<endl;

    // taking input using for loop
    for(int i=0; i < 5; i++){
        int x;
        cin>>x;
        cout<<x<<endl;
    }



}